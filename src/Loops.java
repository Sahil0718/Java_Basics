public class Loops {
    //code that runs multiple times based on a condition. Repeated execution of codes.
    //loop automates repetitive tasks
    //Types of loops: while,for,do-while
    public static void main(String[] args) {
        int num = 1;
        while (num <= 5) {
            System.out.println(num);
            num++;
        }
    }
}
